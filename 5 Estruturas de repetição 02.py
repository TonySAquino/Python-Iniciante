#o while é ideal para quando não sabemos qual o limite que o programa irá percorrer,
#como por exemplo quantos alunos tem em um sala. uma vez que sabemos, podemos 
#utilizar o for; exemplo abaixo:

for i in range(1, 10):      #aqui eu estou falando que, em uma lista que vai de 1 até 9, visto que o último número é sempre 
    print(i)                #o anterior ao declarado, no caso o declarado foi 10, então será nove, a variável i que foi criada
                            #dentro do for irá percorrer essa lista de 1 a 9

                            #no comando logo a seguir, irá imprimir o i na posição da lista que ele está